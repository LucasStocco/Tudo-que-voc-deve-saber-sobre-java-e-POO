import java.util.Arrays;
public class ArraysOrdening {
    public static void main(String[] args) {
        int[] numbers = {5, 1, 4, 2, 3};
        Arrays.sort(numbers); //Ordenando a array
        System.out.println("Vetor ordenado: " + Arrays.toString(numbers));
    }
}

//Podemos ordendar um vetor usando a classe Arrays.
