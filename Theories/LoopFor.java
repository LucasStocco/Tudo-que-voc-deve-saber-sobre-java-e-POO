public class LoopFor {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println("Número: " + i);
        }
    }
}
//Loop For:

//O loop for é utilizado quando você sabe de antemão o número de vezes que deseja repetir um bloco de código. Ele é composto por três partes:

//Initialization: Define uma variável de controle.
//Condicition: Define se o loop continuará a ser executado.
//Increment/Decrement: Modifica a variável de controle a cada iteração.
