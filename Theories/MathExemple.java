public class MathExemple {
    public static void main(String[] args) {
        System.out.println("Valor absoluto de -10: " + Math.abs(-10));
        System.out.println("2^3: " + Math.pow(2, 3));
        System.out.println("Raiz quadrada de 25: " + Math.sqrt(25));
        System.out.println("Máximo enrte 5 e 8: " + Math.max(5, 8));
        System.out.println("Número aleatório entre 0 e 10: " + Math.random() * 10);
        System.out.println("Cosseno de 180 graus: " + Math.cos(Math.toRadians(180)));
    }
}
