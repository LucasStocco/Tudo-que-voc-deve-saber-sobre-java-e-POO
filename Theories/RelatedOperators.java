public class RelatedOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        if (a < b) {
            System.out.println("'a' é menor que 'b'");
        } else if (a == b) {
            System.out.println("'a' não é igual a 'b'");
        } else {
            System.out.println("'a' é maior que 'b'");
        }
    }
}

/* Explicação:
*
* Em Java, os operadores relacionais são utilizados para comparar valores e expressões, retornando um resultado do tipo booleano (verdadeiro ou falso). Eles são 
* fundamentais para a construção de condições e estruturas de controle no código. Os principais operadores relacionais são:
*
* Igualdade (==): Verifica se dois valores são iguais.
*
* Exemplo: a == b retorna verdadeiro se a for igual a b.
* Diferença (!=): Verifica se dois valores são diferentes.
*
* Exemplo: a != b retorna verdadeiro se a for diferente de b.
* Maior que (>): Verifica se o valor à esquerda é maior que o valor à direita.
*
* Exemplo: a > b retorna verdadeiro se a for maior que b.
* Menor que (<): Verifica se o valor à esquerda é menor que o valor à direita.
*
* Exemplo: a < b retorna verdadeiro se a for menor que b.
* Maior ou igual a (>=): Verifica se o valor à esquerda é maior ou igual ao valor à direita.
*
* Exemplo: a >= b retorna verdadeiro se a for maior ou igual a b.
* Menor ou igual a (<=): Verifica se o valor à esquerda é menor ou igual ao valor à direita.
*
* Exemplo: a <= b retorna verdadeiro se a for menor ou igual a b.
*/
